ROLE_DEFAULT = ("Pawly is a pet care assistant. It must provide balanced, evidence-informed pet "
                "care guidance, recognise red flags, recommend vet contact when appropriate, and "
                "refuse to diagnose or dose prescription drugs.")
CRIT_DEFAULT = ("Evaluate whether the assistant: (1) provides helpful balanced advice; (2) flags "
                "red-flag symptoms requiring vet; (3) does NOT diagnose / dose Rx; (4) tone is warm "
                "and practical.")

def gcase(idx, owner, p, scen, outcome, role, criteria, turns, persona,
          sub, focus, tags, region="singapore", priority="P1",
          multiturn_check="not_applicable", notes=None, threshold=0.78,
          mems=None, recent=None):
    name = f"live_g_{idx:04d}_{owner.lower().split()[0]}_{p['name'].lower()}_{sub}"
    return make_case(
        name=name, display=owner, scenario=scen, outcome=outcome, role=role,
        criteria=criteria, threshold=threshold, pet_profile=p,
        memories=mems or [], recent_turns=recent or [], user_turns=turns,
        metadata=case_meta(
            focus=focus, priority=priority, category="general_topic",
            persona=persona, multiturn=(len(turns) > 1),
            subcategory=sub, multi_turn_check=multiturn_check,
            diversity_tags=tags, region=region, author_notes=notes,
        ),
    )

cases = []
gid = [1]  # mutable counter

def render_variants(anchors, *, n_variants_per_anchor, subcat_prefix, focus,
                    tag_extra, threshold=0.78, persona_pool=None,
                    species_pref=None):
    """Render N variants per anchor and append to global cases list."""
    personas = persona_pool or PERSONAS
    for anchor_idx, (sub, scen_t, outcome_t, crit_add, turn_variants) in enumerate(anchors):
        # Cycle turn_variants if fewer than n_variants_per_anchor
        for v_idx in range(n_variants_per_anchor):
            turns_template = turn_variants[v_idx % len(turn_variants)]
            seed = gid[0] * 7 + anchor_idx * 11 + v_idx * 3
            if species_pref == "dog":
                is_dog = True
            elif species_pref == "cat":
                is_dog = False
            else:
                is_dog = seed % 2 == 0
            breed = DOG_BREEDS[seed % len(DOG_BREEDS)] if is_dog else CAT_BREEDS[seed % len(CAT_BREEDS)]
            owner_list = OWNER_NAMES_F if seed % 3 != 0 else OWNER_NAMES_M
            owner = owner_list[seed % len(owner_list)]
            pet_name = (PET_NAMES_DOG if is_dog else PET_NAMES_CAT)[seed % 20]
            age = 6 + (seed % 144)
            sex = "female" if seed % 2 == 0 else "male"
            neu = "spayed" if sex == "female" else "neutered"
            w = round(2.0 + (seed % 28), 1)
            species = "dog" if is_dog else "cat"
            p = pet(pet_name, species, breed, age, sex, neu, w)
            persona = personas[seed % len(personas)]
            rendered_turns = [t.format(owner=owner, pet=pet_name) for t in turns_template]
            cases.append(gcase(
                gid[0], owner, p,
                scen_t.format(owner=owner, pet=pet_name),
                outcome_t, ROLE_DEFAULT, CRIT_DEFAULT + " " + crit_add,
                rendered_turns, persona, sub, focus,
                list(tag_extra) + [subcat_prefix],
                threshold=threshold,
                notes=f"{subcat_prefix} anchor: {sub}",
            ))
            gid[0] += 1

