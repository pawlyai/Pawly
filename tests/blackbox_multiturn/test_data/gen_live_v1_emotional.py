def ecase(idx, owner, p, scen, outcome, role, criteria, mems, recent, turns, persona,
          sub, focus, tags, multiturn_check="not_applicable", notes=None, threshold=0.78):
    name = f"live_em_{idx:03d}_{owner.lower().split()[0]}_{p['name'].lower()}_{sub}"
    return make_case(
        name=name, display=owner, scenario=scen, outcome=outcome, role=role,
        criteria=criteria, threshold=threshold, pet_profile=p,
        memories=mems, recent_turns=recent, user_turns=turns,
        metadata=case_meta(
            focus=focus, priority="P1", category="emotional_safety",
            persona=persona, multiturn=(len(turns) > 1),
            subcategory=sub, multi_turn_check=multiturn_check,
            diversity_tags=tags, region="singapore", author_notes=notes,
        ),
    )

